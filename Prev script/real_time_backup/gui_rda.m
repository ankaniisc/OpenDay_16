chanlocs=readlocs(locpath);
handles.locpath=locpath;
axes(handles.axes1);
handles.chanlocinfo=chanlocs;
topoplot([],chanlocs,'electrodes','numbers','colormap','jet');
hold on;
nChans= numel(chanlocs);
handles.nChans=nChans;
if (nChans==32)
    for k=1:32
        genarr{k}=num2str(k);
    end
end
if (nChans==64)
    
    for k=1:64
        genarr{k}=num2str(k);
    end
    
end
set(handles.popupmenu1,'String',genarr);
